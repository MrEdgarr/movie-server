const pool = require("../config/database");
const citysController = {
    getAll: async (req, res) => {
        try {
            const [rows, fields] = await pool.query(
                "SELECT tickets.* from tickets"
            );
            res.json({
                status: 200,
                message: "Get data has successfully",
                data: rows,
            });
        } catch (error) {
            console.log(error);
            res.json({
                status: "error",
            });
        }
    },
    getById: async (req, res) => {
        try {
            const { id } = req.params;
            const [rows, fields] = await pool.query(
                "SELECT * FROM `tickets` where id = ?",
                [id]
            );
            res.json({
                status: 200,
                message: "Get data has successfully",
                data: rows,
            });
        } catch (error) {
            console.log(error);
            res.json({
                status: "error",
            });
        }
    },
    create: async (req, res) => {
        try {
            const { price, schedules_id } = req.body;
            const sql =
                "INSERT INTO `tickets`( `price`, `schedules_id`) VALUES (?,?)";
            const [rows, fields] = await pool.query(sql, [price, schedules_id]);
            res.json({
                status: 200,
                message: "Get data has successfully",
                data: rows,
            });
        } catch (error) {
            console.log(error);
            res.json({
                status: "error",
            });
        }
    },
    update: async (req, res) => {
        try {
            const { price, schedules_id } = req.body;
            const { id } = req.params;
            const sql =
                "UPDATE `tickets` SET `price`='?', `schedules_id`='?' WHERE id = ?";
            const [rows, fields] = await pool.query(sql, [
                price,
                schedules_id,
                id,
            ]);
            res.json({
                status: 200,
                message: "Get data has successfully",
                data: rows,
            });
        } catch (error) {
            console.log(error);
            res.json({
                status: "error",
            });
        }
    },
    delete: async (req, res) => {
        try {
            const { id } = req.params;
            const [rows, fields] = await pool.query(
                "DELETE FROM `tickets` WHERE id = ?",
                [id]
            );
            res.json({
                status: 200,
                message: "Get data has successfully",
                data: rows,
            });
        } catch (error) {
            console.log(error);
            res.json({
                status: "error",
            });
        }
    },
};

module.exports = citysController;
